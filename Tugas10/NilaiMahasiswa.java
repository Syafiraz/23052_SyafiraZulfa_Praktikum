    public final void viewDataTabel(){
        String [] namakolom = {"NIM", "Nama", "Alamat", "Mata Kuliah", "Nilai Akhir"};
        Object [][] objekmahasiswa = new Object[input.getALL().size()] [5];

        int i = 0;
           for (Iterator<DataMahasiswa> it = input.getALL().iterator(); it.hasNext();) {
               DataMahasiswa dmhs = it.next();
               String arrayMahasiswa[] = {
                   dmhs.getNIM(),
                   dmhs.getNama(),
                   dmhs.getAlamat(),
                   dmhs.getMataKuliah(),
                   String.valueOf(dmhs.getNilaiAkhir())
               };     
               objekmahasiswa[i] = arrayMahasiswa;
               i++;
           }
           modelmahasiswa = new DefaultTableModel(objekmahasiswa, namakolom);
           MahasiswaTB.setModel(modelmahasiswa); 
        }
    
    private void calculateAndDisplayNilaiAkhir() {
    try {
        double nilai1 = Double.parseDouble(Nilai1.getText());
        double nilai2 = Double.parseDouble(Nilai2.getText());
        double nilai3 = Double.parseDouble(Nilai3.getText());
        double nilai4 = Double.parseDouble(Nilai4.getText());
        double nilai5 = Double.parseDouble(Nilai5.getText());

        // Hitung nilai akhir berdasarkan bobot yang diberikan
        double nilaiAkhir = (nilai1 * 0.1) + (nilai2 * 0.15) + (nilai3 * 0.25) + (nilai4 * 0.15) + (nilai5 * 0.35);
        NilaiAkhir.setText(String.valueOf(nilaiAkhir));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Masukkan nilai yang valid!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
