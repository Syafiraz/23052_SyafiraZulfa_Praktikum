    private void SimpanBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanBTActionPerformed
        // TODO add your handling code here:
        calculateAndDisplayNilaiAkhir();
        
        input.insertData(
        NIM.getText(),
        Nama.getText(),
        Alamat.getText(),
        MK.getText()
        );
        
        int lastIndex = input.getALL().size() - 1;
        double nilai1 = Double.parseDouble(Nilai1.getText());
        double nilai2 = Double.parseDouble(Nilai2.getText());
        double nilai3 = Double.parseDouble(Nilai3.getText());
        double nilai4 = Double.parseDouble(Nilai4.getText());
        double nilai5 = Double.parseDouble(Nilai5.getText());
        
        input.setNilai(lastIndex, nilai1, nilai2, nilai3, nilai4, nilai5);
        
        double nilaiAkhir = input.getNilaiAkhir(lastIndex);
        NilaiAkhir.setText(String.valueOf(nilaiAkhir)); // Display final grade

        viewDataTabel(); // Refresh the table
        ClearTextField();
    }//GEN-LAST:event_SimpanBTActionPerformed

